# {{soju:get_hostname_from_canonical_url(node)}}
def get_hostname_from_canonical_url(node):
    """
    Get the hostname portion from the Canonical-URL, and return it.

    If the Canonical-URL header is not set, this returns "localhost".

    """

    # if we have a Canonical-URL header
    if node.has_header("canonical-url"):
        canonical_url = node.get_header("canonical-url")

        # and it looks like it contains a canonical URL
        if "://" in canonical_url:
            # get the host (and maybe :port portion)
            host_and_maybe_port = canonical_url.split("://", 1)[1]

            # return the host portion (without the port, if present)
            return host_and_maybe_port.split(":", 1)[0]

    # if all else fails
    return "localhost"

# N.B. this sort of thing isn't generally recommended on normal sites
#      this function allows demonstrating lots of different node method calls
#      from the reference section at the bottom of each page on this site
def node_method(node, method_name):
    return escape(str(eval("node." + method_name + "()")))

